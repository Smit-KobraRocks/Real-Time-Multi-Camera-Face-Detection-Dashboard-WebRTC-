func drawText(img *image.RGBA, x, y int, text string, col color.Color) {
	for _, ch := range text {
		glyph, ok := glyphs[ch]
		if !ok {
			glyph = glyphs['?']
		}
		for row, line := range glyph {
			for colIdx, c := range line {
				if c == '#' {
					img.Set(x+colIdx, y+row, col)
				}
			}
		}
		x += len(glyph[0]) + 1
	}
}
